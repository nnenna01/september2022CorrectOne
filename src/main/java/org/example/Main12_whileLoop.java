package org.example;

public class Main12_whileLoop {
    public static void main(String[] args) {
//        while loop to print out 10-25
        int A = 10;
        while (A < 26) {
            System.out.println(A);
            A++;


        }
        int B = 20;
        while (B > 10) {
            System.out.println(B);
            B--;
        }
//} the Do while loop
        int c = 10;
        do {
            System.out.println(c);
            c++;

        }while(c<26);


    }
}